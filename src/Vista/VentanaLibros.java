    private void btnActualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarMouseClicked
        int Id_Libro = Integer.parseInt(txt_id_libro.getText());
        String Titulo = txt_titulo.getText();
        String Editorial = txt_editorial.getText();
        String Area = txt_area.getText();
        if (!"".equals(txt_id_libro.getText()) && !"".equals(txt_titulo.getText())
                && !"".equals(txt_editorial.getText()) && !"".equals(txt_area.getText())) {
            dblibro.update(Titulo, Editorial, Area, Id_Libro);
            if (dblibro.correcto) {
                new Suces(this, true).setVisible(true);
            } else {
                new rojerusan.RSNotifyShadowFade("ERROR","HA OCURRIDO UN ERROR" , 7, RSNotifyShadowFade.PositionNotify.TopLef, RSNotifyShadowFade.TypeNotify.ERROR).setVisible(true);       
            }
        } else {
                new rojerusan.RSNotifyShadowFade("ATENCION","POR FAVOR RELLENE TODOS LOS CAMPOS" , 7, RSNotifyShadowFade.PositionNotify.TopLef, RSNotifyShadowFade.TypeNotify.WARNING).setVisible(true);
        }

    }//GEN-LAST:event_btnActualizarMouseClicked

