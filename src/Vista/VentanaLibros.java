    public void SelectLibros() {
        libros = dblibro.select();
        DefaultTableModel tb = (DefaultTableModel) rSTableMetro1.getModel();
        for (AtributosLibro in : libros) {
            tb.addRow(new Object[]{in.getId_Libro(), in.getTitulo(), in.getEditorial(), in.getArea()});
        }
    }

    public void LimpiarDatos() {
        DefaultTableModel tb = (DefaultTableModel) rSTableMetro1.getModel();
        for (int i = tb.getRowCount() - 1; i >= 0; i--) {
            tb.removeRow(i);
        }
    }

