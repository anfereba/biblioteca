    private void jLabel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel4MouseClicked
        VentanaEditarUsuario ventana = new VentanaEditarUsuario();
        DefaultTableModel tb = (DefaultTableModel) rSTableMetro1.getModel();
        try {
            int selectedRowIndex = rSTableMetro1.getSelectedRow();
            ventana.txt_eid_Lector.setText(tb.getValueAt(selectedRowIndex, 0).toString());
            ventana.txt_CC.setText(tb.getValueAt(selectedRowIndex, 1).toString());
            ventana.txt_nombre.setText(tb.getValueAt(selectedRowIndex, 2).toString());
            ventana.txt_apellido.setText(tb.getValueAt(selectedRowIndex, 3).toString());
            ventana.txt_eDireccion.setText(tb.getValueAt(selectedRowIndex, 4).toString());
            ventana.txt_carrera.setText(tb.getValueAt(selectedRowIndex, 5).toString());
            ventana.txt_fecha_nac.setText(tb.getValueAt(selectedRowIndex, 6).toString());
            ventana.txt_eTelefono.setText(tb.getValueAt(selectedRowIndex, 7).toString());
            ventana.txt_Email.setText(tb.getValueAt(selectedRowIndex, 8).toString());
            ventana.txt_ePassword.setText(tb.getValueAt(selectedRowIndex, 9).toString());
            ventana.txt_eEstado.setText(tb.getValueAt(selectedRowIndex, 10).toString());
            this.dispose();
            ventana.setVisible(true);

        } catch (ArrayIndexOutOfBoundsException e) {
            System.out.println(e);
            ventana.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jLabel4MouseClicked
 
