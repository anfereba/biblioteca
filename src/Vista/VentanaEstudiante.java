    private void cbx_buscarestudianteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbx_buscarestudianteActionPerformed
        String c1 = (String) cbx_buscarestudiante.getSelectedItem();
        if (c1.equals("Buscar Por ID")) {
            LimpiarDatos();
            BuscarPorId();
        } else if (c1.equals("Buscar Por CC")) {
            LimpiarDatos();
            BuscarPorCC();
        } else if (c1.equals("Buscar Por NOMBRE")) {
            LimpiarDatos();
            BuscarPorNombre();
        } else if (c1.equals("Buscar Por EMAIL")) {
            LimpiarDatos();
            BuscarPorEmail();
        }    
    }//GEN-LAST:event_cbx_buscarestudianteActionPerformed

