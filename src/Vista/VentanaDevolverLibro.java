    private void btn_regresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_regresarActionPerformed
        String usuario = lbl_usr.getText();
        if (usuario.equals("root")) {
            VentanaPrincipal ventana = new VentanaPrincipal();
            ventana.setVisible(true);
            ventana.label_nombre.setText(usuario);
            this.dispose();
        } else {
            VentanaPrincipalBibliotecologo ventana = new VentanaPrincipalBibliotecologo();
            ventana.setVisible(true);
            ventana.label_usuario.setText(usuario);
            this.dispose();
        }
    }//GEN-LAST:event_btn_regresarActionPerformed

    private void rSTableMetro1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rSTableMetro1MouseClicked
       DefaultTableModel tb = (DefaultTableModel) rSTableMetro1.getModel();
       int selectedRowIndex = rSTableMetro1.getSelectedRow();
           txt_Id_Costeo.setText(tb.getValueAt(selectedRowIndex, 0).toString());
           txt_N_Prestamo.setText(tb.getValueAt(selectedRowIndex, 1).toString());
           txt_Id_Reserva.setText(tb.getValueAt(selectedRowIndex, 3).toString());
    }//GEN-LAST:event_rSTableMetro1MouseClicked

    private void btn_vallidarprestamoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_vallidarprestamoMouseClicked
        VistaDevolverLibroJDBC dbdevolver = new VistaDevolverLibroJDBC();
        int Id_Costeo = Integer.parseInt(txt_Id_Costeo.getText());
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String Fecha= sdf.format(date_fecha_retornado.getDatoFecha());
        dbdevolver.DevolverLibro(Fecha,Id_Costeo);
        JOptionPane.showMessageDialog(null, "Libro devuelto");
    }//GEN-LAST:event_btn_vallidarprestamoMouseClicked

