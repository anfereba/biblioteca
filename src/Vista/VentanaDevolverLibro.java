        public void SelectPrestamos(){
        List<AtributosVistaDevolverLibro> devolverlibro;
        VistaDevolverLibroJDBC dbdevolver = new VistaDevolverLibroJDBC();
        devolverlibro = dbdevolver.select();
        DefaultTableModel tb = (DefaultTableModel) rSTableMetro1.getModel();
        for (AtributosVistaDevolverLibro in : devolverlibro) {
            tb.addRow(new Object[]{in.getId_Costeo(), in.getN_Prestamo(), in.getId_Libro(), in.getId_Reserva(), in.getNombre(), in.getApellido(), in.getNombreLibro(), in.getFecha_Prestamo(), in.getFecha_Devuelto(), in.getFecha_Retornado()});
        }  
    }
         public void LimpiarDatos() {
        DefaultTableModel tb = (DefaultTableModel) rSTableMetro1.getModel();
        for (int i = tb.getRowCount() - 1; i >= 0; i--) {
            tb.removeRow(i);
        }
    }

