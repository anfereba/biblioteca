package Domain;
public class AtributosMulta {
    private int Id_lector, Id_Libro,Total;
    private String Nombre, Apellido, Titulo, Fecha_Prestamo, Fecha_Devuelto, Fecha_Retornado ;

    public int getId_lector() {
        return Id_lector;
    }

    public void setId_lector(int Id_lector) {
        this.Id_lector = Id_lector;
    }

    public int getId_Libro() {
        return Id_Libro;
    }

    public void setId_Libro(int Id_Libro) {
        this.Id_Libro = Id_Libro;
    }

    public int getTotal() {
        return Total;
    }

    public void setTotal(int Total) {
        this.Total = Total;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public String getApellido() {
        return Apellido;
    }

    public void setApellido(String Apellido) {
        this.Apellido = Apellido;
    }

    public String getTitulo() {
        return Titulo;
    }

    public void setTitulo(String Titulo) {
        this.Titulo = Titulo;
    }

    public String getFecha_Prestamo() {
        return Fecha_Prestamo;
    }

    public void setFecha_Prestamo(String Fecha_Prestamo) {
        this.Fecha_Prestamo = Fecha_Prestamo;
    }

    public String getFecha_Devuelto() {
        return Fecha_Devuelto;
    }

    public void setFecha_Devuelto(String Fecha_Devuelto) {
        this.Fecha_Devuelto = Fecha_Devuelto;
    }

    public String getFecha_Retornado() {
        return Fecha_Retornado;
    }

    public void setFecha_Retornado(String Fecha_Retornado) {
        this.Fecha_Retornado = Fecha_Retornado;
    }
    
    
}
